-- Phase 2: insert redirects
INSERT INTO public.redirects (source_path, target_path, status_code, enabled, notes)
SELECT
  source_path,
  CASE reason WHEN 'R' THEN '/category/rfp' WHEN 'A' THEN '/category/pr-firms' ELSE '/' END,
  CASE reason WHEN 'H' THEN 410 ELSE 301 END,
  true,
  'bulk cleanup 2026-05'
FROM _bulk_cleanup_data
ON CONFLICT (source_path) DO NOTHING;

-- Phase 3: collect posts to delete
CREATE TEMP TABLE _del_posts ON COMMIT DROP AS
SELECT p.id, p.featured_media_id
FROM public.posts p
JOIN _bulk_cleanup_data d ON d.source_path = '/' || p.slug;

CREATE TEMP TABLE _del_media ON COMMIT DROP AS
SELECT DISTINCT featured_media_id AS id FROM _del_posts WHERE featured_media_id IS NOT NULL;

-- Phase 4: persist storage paths so they can be purged from the bucket later
CREATE TABLE IF NOT EXISTS public._bulk_cleanup_media_purge (
  media_id bigint PRIMARY KEY,
  storage_path text,
  url text,
  logged_at timestamptz NOT NULL DEFAULT now()
);
INSERT INTO public._bulk_cleanup_media_purge (media_id, storage_path, url)
SELECT m.id, m.storage_path, m.url
FROM public.media m
JOIN _del_media dm ON dm.id = m.id
ON CONFLICT (media_id) DO NOTHING;

-- Phase 5: delete child rows
DELETE FROM public.post_categories WHERE post_id IN (SELECT id FROM _del_posts);
DELETE FROM public.post_tags        WHERE post_id IN (SELECT id FROM _del_posts);
DELETE FROM public.post_revisions   WHERE post_id IN (SELECT id FROM _del_posts);
DELETE FROM public.internal_links
  WHERE source_post_id IN (SELECT id FROM _del_posts)
     OR target_post_id IN (SELECT id FROM _del_posts);
DELETE FROM public.seo_meta
  WHERE object_type = 'post'
    AND object_id IN (SELECT id FROM _del_posts);

-- Phase 6: delete the posts
DELETE FROM public.posts WHERE id IN (SELECT id FROM _del_posts);

-- Phase 7: delete orphaned media
DELETE FROM public.media_variants
  WHERE media_id IN (
    SELECT id FROM _del_media
    WHERE id NOT IN (SELECT featured_media_id FROM public.posts WHERE featured_media_id IS NOT NULL)
  );
DELETE FROM public.media
  WHERE id IN (
    SELECT id FROM _del_media
    WHERE id NOT IN (SELECT featured_media_id FROM public.posts WHERE featured_media_id IS NOT NULL)
  );
